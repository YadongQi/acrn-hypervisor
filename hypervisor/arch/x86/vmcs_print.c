void print_vmcs_all(void)
{
	vmcs_field_t i =0;
	uint64_t val;

	for (i = 0; i < ACRN_VMCS_FIELD_COUNT; ++i) {
		val = exec_vmread(g_field_data[i].encoding);
		printf("%40s (0x%04X) = hw_value(0x%llX)\n", g_field_data[i].name, g_field_data[i].encoding, val);
	}
}
